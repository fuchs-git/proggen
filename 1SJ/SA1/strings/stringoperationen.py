# 9. 1 x 1 dreieck
# -----------------------------------------------------------------------------------------------------

def dreieck(a,b):
    for y in range(a,b+1):
        for i in range(a,b+1):
            if y > i:
                print(f'{' ':<12}\t', end='')
            else:
                print(f'{y:<3}* {i:^3}= {y * i:>3}\t', end='')
        print()

